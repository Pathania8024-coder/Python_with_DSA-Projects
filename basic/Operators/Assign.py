a = int(input("Enter a number: "))
b = int(input("Enter another number: "))

# Assignment
a += b                                    # a = a + b
print("Result:", a)
a -= b                                    # a = a - b
print("Result:", a)
a *= b                                    # a = a * b
print("Result:", a)
a /= b                                    # a = a / b
print("Result:", a)
a %= b  # a = a % b
print("Result:", a)
