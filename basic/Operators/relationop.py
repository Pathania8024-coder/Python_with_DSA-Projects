# variable declaration

a = int(input("Enter the first number: "))
b = int(input("Enter the second number: "))


print("Equal to:", a == b)

print("Not equal to:", a != b)

print("Less than:", a < b)

print("Greater than:", a > b)

print("Less than or equal to:", a <= b)

print("Greater than or equal to:", a >= b)
